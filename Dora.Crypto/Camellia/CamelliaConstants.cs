    // S-box 4: SBOX1 rotated left by 1 bit, then XOR with 0x73
    private static readonly byte[] SBOX4 = new byte[256];

    static CamelliaConstants()
    {
        for (int i = 0; i < 256; i++)
        {
            byte val = SBOX1[i];
            SBOX2[i] = (byte)((val << 1) | (val >> 7));
            SBOX3[i] = (byte)((val >> 1) | (val << 7));
            SBOX4[i] = (byte)(((val << 1) | (val >> 7)) ^ 0x73);
        }
    }

